    public class Solution {
        public boolean hasCycle(ListNode head) {
            // éå†é“¾è¡¨, ä½¿ç”¨HashSetåˆ¤æ–­æ˜¯å¦å·²ç»å­˜åœ¨å³å¯
            Set<ListNode> heads = new HashSet<>();
            while (head != null) {
                if (heads.contains(head))
                    return true;
                heads.add(head);
                head = head.next;
            }
            return false;
        }
    }
